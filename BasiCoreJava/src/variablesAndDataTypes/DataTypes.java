package variablesAndDataTypes;

public class DataTypes {

	public static void main(String[] args) 
	{
		
		
		//integer
		int age=30;
		System.out.println(age);
		
		//Character
		char gender='M';
		
		
		//Decimal Values
		double marks=74.66;
		float mark=67.87f;
		//Boolean
		boolean status=true;
		
		//Strings
		String name="Daya";
		
		
		//For integer 
		byte a=20;
		short b=10;
		int c=30;
		
		
	}

}
